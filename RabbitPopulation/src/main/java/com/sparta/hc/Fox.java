package com.sparta.hc;

public class Fox {

    private int age;
    private int breedingTime;
    private int gender;
    private int foxBabies;
    private int breedingMaturity;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getBreedingTime() {
        return breedingTime;
    }

    public void setBreedingTime(int breedingTime) {
        this.breedingTime = breedingTime;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public int getFoxBabies() {
        return foxBabies;
    }

    public void setFoxBabies(int foxBabies) {
        this.foxBabies = foxBabies;
    }

    public int getBreedingMaturity() {
        return breedingMaturity;
    }

    public void setBreedingMaturity(int breedingMaturity) {
        this.breedingMaturity = breedingMaturity;
    }
}
